const sqlite = require(`sqllite3`);
const db = new sqlite.Database(`travel.db`);

const createTripTable = `CREATE TABLE IF NOT EXISTS TRIP (
ID INTEGER PRIMARY KEY AUTOINCREMENT,
DESTINATION TEXT NOT NULL,
LOCATION TEXT NOT NULL,
CONTINENT TEXT NOT NULL,
LANGUAGE TEXT NOT NULL,
DESCRIPTION TEXT NOT NULL,
FLIGHTCOAST REAL DEFAULT 0,
ACCOMMODATIONCOAST REAL DEFAULT 0,
MEALCOAST REAL DEFAULT 0,
)`;

module.EXPORTS = {
    db,
    createTripTable,
};